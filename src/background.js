const pagePrefix = "https://commons.wikimedia.org/wiki/File:";

const urlToCSSValue = (url) => `url("${url}")`;
const resizeMediaWiki = (url, maxWidth = null) => `${indexSpecialRedirectPrefix}${encodeURIComponent(url)}&width=${maxWidth}`;

const getWidth = function() {
    return Math.max(
        document.body.scrollWidth,
        document.documentElement.scrollWidth,
        document.body.offsetWidth,
        document.documentElement.offsetWidth,
        document.documentElement.clientWidth,
        window.innerWidth,
        // window.outerWidth,
    );
};
const possibleWidths = [1280, 1600, 1920, 2048, 2560, 3200, 4096];
const width = getWidth();
const desiredWidth = possibleWidths.find((n) => n >= width) || 4096;

